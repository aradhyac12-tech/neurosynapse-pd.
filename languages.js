function t(key) {
    const lang = LANGUAGES[currentLanguage] || LANGUAGES['en'];
    return lang[key] || key;
}

function updateLanguage() {
    const lang = LANGUAGES[currentLanguage] || LANGUAGES['en'];
    
    // Update all text elements by ID
    for (const [key, value] of Object.entries(lang)) {
        const element = document.getElementById(key);
        if (element && typeof value === 'string') {
            element.textContent = value;
        }
    }
    
    // Update country dropdown
    const countrySelect = document.getElementById('country');
    if (countrySelect) {
        const selectedValue = countrySelect.value;
        countrySelect.innerHTML = '<option value="">Select</option>';
        
        for (const [code, name] of Object.entries(lang.countries || {})) {
            const option = document.createElement('option');
            option.value = code;
            option.textContent = name;
            countrySelect.appendChild(option);
        }
        
        if (selectedValue) {
            countrySelect.value = selectedValue;
        }
    }
    
    localStorage.setItem('language', currentLanguage);
}

// Initialize on load
document.addEventListener('DOMContentLoaded', updateLanguage);
